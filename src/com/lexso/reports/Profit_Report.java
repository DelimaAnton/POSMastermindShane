    public void loadSalesDetails() {

        Double salesTotal = 0.0;
        Double costTotal = 0.0;
        Double totalQty = 0.0;
        Double netProfit = 0.0;

        boolean hasResult = false;

        int month = jMonthChooser1.getMonth() + 1;
        int year = jYearChooser1.getYear();

        String query = "SELECT * FROM `invoice` "
                + "INNER JOIN `invoice_item` ON `invoice`.`id` = `invoice_item`.`invoice_id` "
                + "INNER JOIN `stock` ON `invoice_item`.`stock_id` = `stock`.`id` "
                + "WHERE MONTH(`invoice`.`date`) = '" + month + "' AND YEAR(`invoice`.`date`) = '" + year + "'";

        try {

            ResultSet resultSet = DatabaseConnection.executeSearch(query);

            while (resultSet.next()) {
                hasResult = true;
                double buyingPrice = Double.parseDouble(resultSet.getString("stock.buying_price"));
                double sellingPrice = Double.parseDouble(resultSet.getString("stock.selling_price"));
                double qty = Double.parseDouble(resultSet.getString("invoice_item.qty"));
                totalQty += qty;
                salesTotal += qty * sellingPrice;
                costTotal += qty * buyingPrice;
            }

            if (!hasResult) {
                JOptionPane.showMessageDialog(this, "No sales happened for this month or date", "Information",
                        JOptionPane.INFORMATION_MESSAGE);
            }

            netProfit = salesTotal - costTotal;

            jFormattedTextField1.setText(String.valueOf(salesTotal));
            jFormattedTextField2.setText(String.valueOf(costTotal));
            jFormattedTextField3.setText(String.valueOf(netProfit));
            jFormattedTextField4.setText(String.valueOf(totalQty));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

