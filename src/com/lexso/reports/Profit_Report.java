    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String year = String.valueOf(jYearChooser1.getYear());
        String month = String.valueOf(new DateFormatSymbols().getMonths()[jMonthChooser1.getMonth()]);
        String totalSales = jFormattedTextField1.getText();
        String totalCost = jFormattedTextField2.getText();
        String totalProfit = jFormattedTextField3.getText();
        String totalQty = jFormattedTextField4.getText();

        try {
            InputStream s = this.getClass().getResourceAsStream("/com/lexso/reports/resources/Profit_Report.jasper");

            HashMap<String, Object> params = new HashMap<>();
            params.put("Parameter1", "Profit report for: " + month + " of " + year);
            params.put("Parameter2", String.valueOf(totalSales));
            params.put("Parameter3", String.valueOf(totalCost));
            params.put("Parameter4", String.valueOf(totalProfit));
            params.put("Parameter5", String.valueOf(totalQty));

            JasperPrint jasperPrint
            = JasperFillManager.fillReport(s, params, new JREmptyDataSource());

            JasperViewer.viewReport(jasperPrint, false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

