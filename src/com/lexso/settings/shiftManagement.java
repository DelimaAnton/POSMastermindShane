    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String id = jLabel7.getText();
            String name = txtName.getText();
            String start = txtStart.getText();
            String end = txtEnd.getText();

            DatabaseConnection.executeIUD("UPDATE shift SET "
                    + "shift_name = '" + name + "', "
                    + "shift_start_time = '" + start + "', "
                    + "shift_end_time = '" + end + "' "
                    + "WHERE shift_id = '" + id + "'");

            JOptionPane.showMessageDialog(this, "Shift updated successfully.");
            loadShift();
            clearFields();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred while updating shift.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtName.getText();
        String start = txtStart.getText();
        String end = txtEnd.getText();

        try {
            DatabaseConnection.executeIUD("INSERT INTO shift (shift_name,shift_start_time,shift_end_time) "
                    + "VALUES ('" + name + "', '" + start + ":00', '" + end + ":00')");
        } catch (Exception ex) {
            Logger.getLogger(shiftManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Shift added!");
        clearFields();
        loadShift();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (evt.getClickCount() == 2) {

            jLabel7.setText(model.getValueAt(selectedRow, 0).toString());
            txtName.setText(model.getValueAt(selectedRow, 3).toString());
            txtStart.setText(model.getValueAt(selectedRow, 1).toString());
            txtEnd.setText(model.getValueAt(selectedRow, 2).toString());

        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String shiftId = jTable1.getValueAt(selectedRow, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(
                null,
                "Are you sure you want to delete the shift with ID " + shiftId + "?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            try {

                DatabaseConnection.executeIUD("DELETE FROM `shift` WHERE `shift_id` = '" + shiftId + "'");

                JOptionPane.showMessageDialog(null, "Deletion Successful.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);

                loadShift();

                clearFields();

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error while deleting: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Deletion canceled.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

