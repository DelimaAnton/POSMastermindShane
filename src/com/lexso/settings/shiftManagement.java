    private void loadShift() {
        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT * FROM `shift`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("shift_id"));
                vector.add(resultSet.getString("shift_start_time"));
                vector.add(resultSet.getString("shift_end_time"));
                vector.add(resultSet.getString("shift_name"));

                model.addRow(vector);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

