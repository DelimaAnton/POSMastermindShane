    private void loadExistingLogo() {
        try {
            // Query database for existing logo path
            ResultSet rs = DatabaseConnection.executeSearch("SELECT `logo` FROM `editable_receipt` WHERE `id` = '1'");
            if (rs.next()) {
                String logoPath = rs.getString("logo");
                if (logoPath != null && !logoPath.isEmpty()) {
                    // Try to load the image
                    File logoFile = new File(logoPath);
                    if (logoFile.exists()) {
                        selectedImageFile = logoFile;
                        displayImage(logoFile);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void chooseImage() {
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "png", "jpeg");
        fileChooser.setFileFilter(filter);

        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = fileChooser.getSelectedFile();
            displayImage(selectedImageFile);
        }
    }

    private void displayImage(File imageFile) {
        ImageIcon imageIcon = new ImageIcon(imageFile.getAbsolutePath());

        // Use default dimensions if label hasn't been sized yet
        int width = jLabel8.getWidth() > 0 ? jLabel8.getWidth() : 100; // default width
        int height = jLabel8.getHeight() > 0 ? jLabel8.getHeight() : 100; // default height

        Image scaledImage = imageIcon.getImage().getScaledInstance(
                width,
                height,
                Image.SCALE_SMOOTH);
        jLabel8.setIcon(new ImageIcon(scaledImage));
        jLabel8.setText("");
    }

    private void clearImage() {
        selectedImageFile = null;
        jLabel8.setIcon(null);
        jLabel8.setText("");
    }

