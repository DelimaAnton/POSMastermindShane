public void loadReceipt() {
        try {
            ResultSet rs = DatabaseConnection.executeSearch("SELECT * FROM `editable_receipt` WHERE `id`='1'");
            if (rs.next()) {
                String shopName = rs.getString("shop_name");
                String address = rs.getString("Address");
                String mobile = rs.getString("mobile");
                String email = rs.getString("email");
                String thankNote = rs.getString("thank_note");
                String logoPath = rs.getString("logo");

                jLabel9.setText(shopName);
                jLabel11.setText(address);
                jLabel12.setText(mobile);
                jLabel13.setText(email);
                jLabel24.setText(thankNote);

                // Load and display logo if path exists
                if (logoPath != null && !logoPath.isEmpty()) {
                    File logoFile = new File(logoPath);
                    if (logoFile.exists()) {
                        ImageIcon originalIcon = new ImageIcon(logoPath);

                        // Use default dimensions if label hasn't been sized yet
                        int width = jLabel2.getWidth() > 0 ? jLabel2.getWidth() : 100;
                        int height = jLabel2.getHeight() > 0 ? jLabel2.getHeight() : 100;

                        // Scale maintaining aspect ratio
                        double ratio = Math.min(
                                (double) width / originalIcon.getIconWidth(),
                                (double) height / originalIcon.getIconHeight()
                        );

                        Image scaledImage = originalIcon.getImage().getScaledInstance(
                                (int) (originalIcon.getIconWidth() * ratio),
                                (int) (originalIcon.getIconHeight() * ratio),
                                Image.SCALE_SMOOTH
                        );

                        jLabel2.setIcon(new ImageIcon(scaledImage));
                        jLabel2.setText("");
                    } else {
                        jLabel2.setIcon(null);
                        jLabel2.setText("Logo not found");
                    }
                } else {
                    jLabel2.setIcon(null);
                    jLabel2.setText("No logo set");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            jLabel2.setIcon(null);
            jLabel2.setText("Error loading logo");
        }
    }
