    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (selectedImageFile == null) {
            JOptionPane.showMessageDialog(this, "Please select an image first!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // 1. Determine the resources folder path
            String resourcesPath = "src/com/lexso/settings/resources/";

            // 2. Create resources directory if it doesn't exist
            File resourcesDir = new File(resourcesPath);
            if (!resourcesDir.exists()) {
                resourcesDir.mkdirs();
            }

            // 3. Get the file extension
            String fileName = selectedImageFile.getName();
            String extension = fileName.substring(fileName.lastIndexOf("."));

            // 4. Create destination path (always named "Logo")
            String destPath = resourcesPath + "Logo" + extension;
            File destFile = new File(destPath);

            // 5. Copy the file to resources folder
            Files.copy(selectedImageFile.toPath(), destFile.toPath(), StandardCopyOption.REPLACE_EXISTING);

            // 6. Save the relative path to database
            String relativePath = "src/com/lexso/settings/resources/logo" + extension;
            DatabaseConnection.executeIUD("UPDATE `editable_receipt` SET `logo` = '" + relativePath + "' WHERE `id` = '1'");

            // 7. Show success message
            JOptionPane.showMessageDialog(this, "Logo saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadReceipt();
            clearImage();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving image file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

