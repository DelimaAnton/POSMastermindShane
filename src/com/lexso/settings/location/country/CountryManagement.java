    public void loadCountry(String searchTerm) {
        try {
            String query;
            if (searchTerm.isEmpty()) {
                query = "SELECT * FROM `country` INNER JOIN `status` ON `country`.`status_Id` = `status`.`id`";
            } else {
                query = "SELECT * FROM `country` INNER JOIN `status` ON `country`.`status_Id` = `status`.`id` " +
                        "WHERE `country_name` LIKE '%" + searchTerm + "%'";
            }
            
            ResultSet resultset = DatabaseConnection.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultset.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultset.getString("id"));
                vector.add(resultset.getString("country_name"));
                vector.add(resultset.getString("status.name"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

