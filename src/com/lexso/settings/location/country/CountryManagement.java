    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String countryname = jTextField1.getText();
            ResultSet resultset = DatabaseConnection.executeSearch("SELECT * FROM `country` WHERE `country_name` = '" + countryname + "' ");

            if (countryname.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please Enter Country Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (resultset.next()) {
                JOptionPane.showMessageDialog(this, "This Country Has already exited", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                DatabaseConnection.executeIUD("INSERT INTO `country` (`country_name`,`status_Id`)VALUES('" + countryname + "','1')");
                reset();
                loadCountry();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

