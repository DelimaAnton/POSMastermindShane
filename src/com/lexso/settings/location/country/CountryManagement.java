    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            int row = jTable1.getSelectedRow();
            String id = String.valueOf(jTable1.getValueAt(row, 0));
            ResultSet resultset1 = DatabaseConnection.executeSearch("SELECT * FROM `country` WHERE `id` = '" + id + "' ");

            if (resultset1.next()) {
                int status = resultset1.getInt("status_Id");

                if (status == 1) {
                    DatabaseConnection.executeIUD("UPDATE `country` SET `status_Id` = '2' WHERE `id` = '" + id + "'");

                } else {
                    DatabaseConnection.executeIUD("UPDATE `country` SET `status_Id` = '1' WHERE `id` = '" + id + "'");

                }

                loadCountry();
                reset();

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

