    private void loadBank() {

        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT * FROM `bank`");

            Vector vector = new Vector();

            while (resultSet.next()) {
                vector.add(resultSet.getString("bank_name"));
                bankMap.put(resultSet.getString("bank_name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            bankComboBox.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadEmployeeEmail() {

        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT * FROM `employee`");

            Vector vector = new Vector();

            while (resultSet.next()) {
                vector.add(resultSet.getString("email"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            emailComboBox.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

