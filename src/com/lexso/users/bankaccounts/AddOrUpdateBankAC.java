    private void addOrUpdateBankDetails() {

        try {
            ResultSet bankresultSet = DatabaseConnection.executeSearch("SELECT * FROM `bank_details` "
                    + "INNER JOIN `bank` ON `bank_details`.`bank_id` = `bank`.`id` "
                    + "WHERE `bank_details`.`employee_email` = '" + emailComboBox.getSelectedItem() + "'");

            String name = jTextField1.getText();
            String acnumber = jTextField2.getText();
            String branch = jTextField3.getText();
            String description = jTextArea1.getText();

            String bank_name = String.valueOf(bankComboBox.getSelectedItem());

            if (name.isEmpty() || acnumber.isEmpty() || branch.isEmpty() || description.isEmpty() || bank_name == null) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields.");
                return;
            }

            if (!bankresultSet.next()) {
                DatabaseConnection.executeIUD("INSERT INTO `bank_details`(`name`, `acnumber`, `branch`, `description`, `bank_id`, `employee_email`) VALUES ('" + name + "', '" + acnumber + "', '" + branch + "', '" + description + "', " + bankMap.get(bank_name) + ", '" + emailComboBox.getSelectedItem() + "')");
                JOptionPane.showMessageDialog(this, "Bank details added successfully.");
            } else {

                int response = JOptionPane.showConfirmDialog(this,
                        "Are you sure you want to update the bank details?",
                        "Confirm Update",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (response == JOptionPane.YES_OPTION) {
                    DatabaseConnection.executeIUD("UPDATE `bank_details` SET `name` = '" + name + "', `acnumber` = '" + acnumber + "', `branch` = '" + branch + "', `description` = '" + description + "', `bank_id` = " + bankMap.get(bank_name) + " WHERE `employee_email` = '" + emailComboBox.getSelectedItem() + "'");
                    JOptionPane.showMessageDialog(this, "Bank details updated successfully.");
                } else {
                    JOptionPane.showMessageDialog(this, "Update canceled.");
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void checkBankDetails(String recent) {

        try {

            ResultSet bankresultSet = DatabaseConnection.executeSearch("SELECT * FROM `bank_details` "
                    + "INNER JOIN `bank` ON `bank_details`.`bank_id` = `bank`.`id` "
                    + "WHERE `bank_details`.`employee_email` = '" + emailComboBox.getSelectedItem() + "'");

            if (bankresultSet.next()) {
                if (recent.equals("view")) {
                    jTextField1.setText(bankresultSet.getString("name"));
                    jTextField2.setText(bankresultSet.getString("acnumber"));
                    jTextField3.setText(bankresultSet.getString("branch"));
                    jTextArea1.setText(bankresultSet.getString("description"));

                    bankComboBox.setSelectedItem(bankresultSet.getString("bank_name"));

                    addButton.setVisible(false);
                    addmoreButton.setVisible(false);
                    updateButton.setVisible(false);
                    cancelButton.setVisible(true);

                    emailComboBox.setEditable(false);
                    bankComboBox.setEditable(false);
                    jTextField1.setEditable(false);
                    jTextField2.setEditable(false);
                    jTextField3.setEditable(false);
                    jTextArea1.setEditable(false);
                } else {
                    jTextField1.setText(bankresultSet.getString("name"));
                    jTextField2.setText(bankresultSet.getString("acnumber"));
                    jTextField3.setText(bankresultSet.getString("branch"));
                    jTextArea1.setText(bankresultSet.getString("description"));

                    bankComboBox.setSelectedItem(bankresultSet.getString("bank_name"));

                    addButton.setVisible(false);
                    addmoreButton.setVisible(false);
                    updateButton.setVisible(true);
                    cancelButton.setVisible(true);
                }
            } else {
                if (recent.equals("view")) {
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextArea1.setText("");

                    bankComboBox.setSelectedIndex(0);

                    addButton.setVisible(false);
                    addmoreButton.setVisible(false);
                    updateButton.setVisible(false);
                    cancelButton.setVisible(true);
                } else {
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextArea1.setText("");

                    bankComboBox.setSelectedIndex(0);

                    addButton.setVisible(true);
                    addmoreButton.setVisible(true);
                    updateButton.setVisible(false);
                    cancelButton.setVisible(true);
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

