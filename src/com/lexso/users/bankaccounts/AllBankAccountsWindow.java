    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {

            String userEmail = jTable1.getValueAt(selectedRow, 0).toString();

            int confirmation = JOptionPane.showConfirmDialog(this,
                    "This action will permanently delete the bank details for " + userEmail + ". Do you wish to proceed?",
                    "Confirm Deletion",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirmation == JOptionPane.YES_OPTION) {
                try {

                    DatabaseConnection.executeIUD("DELETE FROM `bank_details` WHERE `user_email` = '" + userEmail + "'");

                    JOptionPane.showMessageDialog(this, "Bank details deleted successfully.");
                    loadBankData("");

                } catch (Exception e) {
                    e.printStackTrace();
                    // Show an error message if something goes wrong
                    JOptionPane.showMessageDialog(this, "Error while deleting the bank details: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            // Show a message if no row is selected
            JOptionPane.showMessageDialog(this, "Please select a row from the table.", "No Selection", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

