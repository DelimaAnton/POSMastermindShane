    public void loadBankData(String search) {

        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT "
                    + "bd.user_email AS email, "
                    + "bd.name, "
                    + "bd.acnumber AS account_number, "
                    + "b.bank_name AS bank, "
                    + "bd.branch AS bank_branch, "
                    + "bd.description "
                    + "FROM `bank_details` bd "
                    + "INNER JOIN `bank` b ON `bd`.`bank_id` = `b`.`id` "
                    + "WHERE bd.user_email LIKE '%" + search + "%' "
                    + "OR bd.name LIKE '%" + search + "%' "
                    + "OR bd.acnumber LIKE '%" + search + "%' "
                    + "OR b.bank_name LIKE '%" + search + "%' "
                    + "OR bd.branch LIKE '%" + search + "%' "
                    + "OR bd.description LIKE '%" + search + "%'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> v = new Vector<>();
                v.add(resultSet.getString("email"));
                v.add(resultSet.getString("name"));
                v.add(resultSet.getString("account_number"));
                v.add(resultSet.getString("bank"));
                v.add(resultSet.getString("bank_branch"));
                v.add(resultSet.getString("description"));

                model.addRow(v);
            }

            jTable1.setModel(model);

            // Centering the text in the table cells
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

